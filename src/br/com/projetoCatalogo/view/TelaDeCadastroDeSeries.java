    public final void readTable() throws SQLException{
        
        DefaultTableModel modelo = (DefaultTableModel) TableSeries.getModel();
        modelo.setNumRows(0);
        SerieDAO seriedao = new SerieDAO();
        
        for(Serie serie: seriedao.read()){
            modelo.addRow(new Object[]{
                serie.getId(),
                serie.getNome(),
                serie.getPlataforma(),
                serie.getCategoria(),
                serie.getTemporadas()
            });
        }
    }

