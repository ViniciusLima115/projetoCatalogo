    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Serie serie = new Serie();
        SerieDAO seriedao = new SerieDAO();
        
        int index = TableSeries.getSelectedRow();//Retrona o numero da linha selecionada
        
        try {
            serie = seriedao.read().get(index);//Retorna o objeto dentro da ArrayList
            
            //Set as novas informacoes para o objeto selecionado
            serie.setNome(jTextFieldNomeSerie.getText());
            serie.setPlataforma(jTextFieldPlataformaSerie.getText());
            serie.setCategoria(jTextFieldCategoriaSerie.getText());
            serie.setTemporadas(jTextFieldTemporadaSerie.getText());
            //faz a alteracao no bd
            seriedao.update(serie);
            readTable();
        } catch (SQLException ex) {
            Logger.getLogger(TelaDeCadastroDeSeries.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void TableSeriesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableSeriesMouseClicked
       Serie serie = new Serie();
       SerieDAO seriedao = new SerieDAO();
       
       int index = TableSeries.getSelectedRow();
       
        try {
            serie = seriedao.read().get(index);
            
            jTextFieldNomeSerie.setText(String.valueOf(serie.getNome()));
            jTextFieldPlataformaSerie.setText(String.valueOf(serie.getPlataforma()));
            jTextFieldCategoriaSerie.setText(String.valueOf(serie.getCategoria()));
            jTextFieldTemporadaSerie.setText(String.valueOf(serie.getTemporadas()));
            
        } catch (SQLException ex) {
            Logger.getLogger(TelaDeCadastroDeSeries.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_TableSeriesMouseClicked

